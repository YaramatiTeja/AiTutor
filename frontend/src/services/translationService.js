export const gradeCodes = ['class4', 'class5', 'class6', 'class7', 'class8', 'class9', 'class10'];
export const subjectCodes = ['science', 'maths', 'english', 'socialScience', 'history', 'geography'];

export const normalizeLanguage = (value) => (translations[value] ? value : 'en');

export const normalizeGrade = (value) => {
  if (gradeCodes.includes(value)) return value;
  const match = String(value || '').match(/(class\s*)?(\d+)/i);
  return match ? `class${match[2]}` : 'class6';
};

export const normalizeSubject = (value) => {
  const normalized = String(value || '').toLowerCase();
  if (subjectCodes.includes(normalized)) return normalized;
  if (normalized.includes('math')) return 'maths';
  if (normalized.includes('science') && !normalized.includes('social')) return 'science';
  if (normalized.includes('english')) return 'english';
  if (normalized.includes('social')) return 'socialScience';
  if (normalized.includes('history')) return 'history';
  if (normalized.includes('geography')) return 'geography';
  return 'science';
};
